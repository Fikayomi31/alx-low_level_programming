#include "main.h"
/**
 * add - addition of number
 * @a: number 1
 * @b: number 2
 *
 * Return: result
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtraction of number
 * @a: number 1
 * @b: number 2
 *
 * Return: result
 */
int sub(int a, int b)
{
	return ( a - b);
}

/**
 * mul - multipication of number
 * @a: number 1
 * @b: number 2
 *
 * Return: result
 */
int mul(int a, int b)
{
	return ( a * b);
}

/**
 * div - division of number
 * @a: number 1
 * @b: number 2
 *
 * Return: result
 */
int div(int a, int b)
{
	return ( a / b);
}

/**
 * mod - modules of number
 * @a: number 1
 * @b: number 2
 *
 * Return: result
 */
int mid(int a, int b)
{
	return ( a % b);
}
